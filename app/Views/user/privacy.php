</ol>

<h3>個人情報の訂正</h3>

<p>
弊社は、お客様の権利を尊重し、個人情報に対する苦情・相談に、適切、かつ迅速な対応を行います。
</p>

<p>
お客様が個人情報保護法の定めに基づいて個人情報について訂正、削除、利用の停止を求められた場合、お客様ご本人からのご請求であることを確認の上で必要な調査を行い、その結果に必要な範囲内において個人情報の訂正等を行い、その旨お客様に通知します。
</p>

